    protected void populaSeletorUsuário(UIGovernor_F UImaster){
        this.Login_SeletorUsuário_JComboBox.removeAllItems();
        
        UImaster.UserBase.getUserList().forEach( (usuário) -> {
            this.Login_SeletorUsuário_JComboBox.addItem(usuário);  //popula a caixa de seleção de usuários
            
        });
        
        if(UImaster.UserBase.getUserList().isEmpty()){
            primeiroCadastro_botão.setEnabled(true);  //trava o botão "primeiro cadastro" se já existirem usuarios cadastrados
            Login_OK_botão.setEnabled(false);
        }
            
        else{
            primeiroCadastro_botão.setEnabled(false);
            Login_OK_botão.setEnabled(true);
        }
            
    }
    
    public Login_P(UIGovernor_F UImaster) {
        this.UImaster = UImaster;
        initComponents();
        populaSeletorUsuário(UImaster);

    }
    
