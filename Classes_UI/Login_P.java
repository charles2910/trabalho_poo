    private void Login_OK_botãoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Login_OK_botãoActionPerformed
        
        Usuário user = ((Usuário) this.Login_SeletorUsuário_JComboBox.getSelectedItem()); //recupera o usuário da caixa de seleção de usuários
        
        if(user != null && user.PasswordMatch(new String(this.Login_Senha_PasswordField.getPassword())) ){
            //testa se a senha digitada bate com a do usuário selecionado
            UImaster.CurrentUser = user;
            UImaster.removeLoginScreen();
            JOptionPane.showMessageDialog(null, "Bem Vindo(a) "+ user.toString() + "!");
            UImaster.menuOPC();    
        }
        else{ 
            JOptionPane.showMessageDialog(null, "Senha Incorreta!"); //caso o método PasswordMatch falhe, o login não prossegue.
        }  
    }//GEN-LAST:event_Login_OK_botãoActionPerformed

